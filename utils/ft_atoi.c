#include "minitalk.h"

int	ft_atoi(char *s)
{
	int	res;
	int	sign;

	while (*s && ((*s >= 9 && *s <= 13) || *s == 32))
		*s++;
	sign = -1 * (*s == 45) + (*s != 45);
	s += (*s == 45 || *s == 43);
	if (!*s)
		return (0);
	res = 0;
	while (*s >= 48 && *s <= 57)
	{
		res = ((res << 3) + (res << 1)) + (*s & 0b00001111);
		s++;
	}
	return (res * sign);
}
